function openForm() {
    document.getElementById("myForm").style.display = "block";
    document.getElementById("reg").style.display = "none";
  }
  
  function closeForm() {
    document.getElementById("myForm").style.display = "none";
    document.getElementById("reg").style.display = "none";
  }

  function opensignup(){
    document.getElementById("reg").style.display = "block";
    document.getElementById("myForm").style.display = "none";
  }